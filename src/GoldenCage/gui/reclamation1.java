    private void bt_reclamationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_reclamationActionPerformed
        // TODO add your handling code here:
        
        List<Reclamation> rec = new ArrayList<Reclamation>();
        ReclamationDAO recdao = new ReclamationDAO();
        
        rec = recdao.afficherReclamation();
        
      
        
        for(Reclamation e : rec)
        {
            
            
            tablerec.getModel().setValueAt(e.getIdreclamation(), 0, 0);
            tablerec.getModel().setValueAt(e.getTexte(), 0, 1);
            tablerec.getModel().setValueAt(e.getEtat(), 0, 2);
            tablerec.getModel().setValueAt(e.getDate(), 0, 3);
            
            
        }
    }//GEN-LAST:event_bt_reclamationActionPerformed

